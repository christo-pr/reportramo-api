import type { Paginated, Params } from '@feathersjs/feathers'
import { MongoDBService } from '@feathersjs/mongodb'
import type { MongoDBAdapterParams, MongoDBAdapterOptions } from '@feathersjs/mongodb'

import type { Application } from '../../declarations'
import type { RoadStatus, RoadStatusData, RoadStatusPatch, RoadStatusQuery } from './road-status.schema'

export type { RoadStatus, RoadStatusData, RoadStatusPatch, RoadStatusQuery }

export interface RoadStatusParams extends MongoDBAdapterParams<RoadStatusQuery> { }

// By default calls the standard MongoDB adapter service methods but can be customized with your own functionality.
export class RoadStatusService<ServiceParams extends Params = RoadStatusParams> extends MongoDBService<
  RoadStatus,
  RoadStatusData,
  RoadStatusParams,
  RoadStatusPatch
> { }

export const getOptions = (app: Application): MongoDBAdapterOptions => {
  return {
    paginate: app.get('paginate'),
    Model: app.get('mongodbClient').then(db => db.collection('RoadStatus'))
  }
}
